class TestNewsSources:
    _src_id = None

    def test_list_seeded(self, auth_client):
        r = auth_client.get(f"{API}/news-sources")
        assert r.status_code == 200
        data = r.json()
        assert len(data) >= 7, f"expected >=7 seeded sources, got {len(data)}"

    def test_create_source(self, auth_client):
        payload = {"name": "TEST_Source", "url": "https://example.com", "rss_url": "https://example.com/rss"}
        r = auth_client.post(f"{API}/news-sources", json=payload)
        assert r.status_code in (200, 201), r.text
        TestNewsSources._src_id = r.json()["id"]

    def test_fetch_missing_rss_graceful(self, auth_client):
        # Create source with unreachable rss
        r = auth_client.post(f"{API}/news-sources", json={
            "name": "TEST_Broken", "url": "https://invalid.invalid", "rss_url": "https://invalid.invalid/rss.xml"
        })
        assert r.status_code in (200, 201)
        sid = r.json()["id"]
        f = auth_client.post(f"{API}/news-sources/{sid}/fetch")
        assert f.status_code == 200, f"Fetch should not crash: {f.status_code} {f.text}"
        data = f.json()
        assert "imported" in data
        # cleanup
        auth_client.delete(f"{API}/news-sources/{sid}")

    def test_cleanup_source(self, auth_client):
        if TestNewsSources._src_id:
            auth_client.delete(f"{API}/news-sources/{TestNewsSources._src_id}")


