class TestGenerator:
    _customer_id = None
    _content_id = None

    def test_get_customer(self, auth_client):
        r = auth_client.get(f"{API}/customers")
        assert r.status_code == 200
        TestGenerator._customer_id = r.json()[0]["id"]

    def test_generate_content(self, auth_client):
        assert TestGenerator._customer_id
        payload = {
            "customer_id": TestGenerator._customer_id,
            "platform": "instagram",
            "tone": "professional",
            "cta": "Jetzt Termin sichern",
        }
        r = auth_client.post(f"{API}/generator/content", json=payload, timeout=90)
        assert r.status_code in (200, 201), r.text
        data = r.json()
        assert data.get("title"), f"Empty title: {data}"
        assert data.get("body"), "empty body"
        assert isinstance(data.get("hashtags", []), list)
        TestGenerator._content_id = data.get("id")

    def test_hashtags(self, auth_client):
        r = auth_client.post(f"{API}/generator/hashtags", json={"text": "Chiptuning und DPF Service", "count": 5}, timeout=60)
        assert r.status_code == 200, r.text
        tags = r.json()
        # Accept either list or dict wrapper
        if isinstance(tags, dict):
            tags = tags.get("hashtags", [])
        assert isinstance(tags, list) and len(tags) > 0
        assert all(str(t).startswith("#") for t in tags), tags

    def test_compliance_check_illegal(self, auth_client):
        r = auth_client.post(
            f"{API}/generator/compliance-check",
            json={"text": "Wir bieten 100% legal DPF off und TÜV garantiert"},
        )
        assert r.status_code == 200
        data = r.json()
        assert data.get("ok") is False
        assert data.get("warnings") or data.get("issues")

    def test_list_generated_contents(self, auth_client):
        r = auth_client.get(f"{API}/generator/contents")
        assert r.status_code == 200
        assert isinstance(r.json(), list)

    def test_update_generated_content(self, auth_client):
        if not TestGenerator._content_id:
            pytest.skip("no content id")
        r = auth_client.put(
            f"{API}/generator/contents/{TestGenerator._content_id}",
            json={"status": "approved"},
        )
        assert r.status_code == 200, r.text
        assert r.json().get("status") == "approved"


